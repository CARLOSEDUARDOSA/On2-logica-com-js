/*

Executar três operações matematicas, solicitadas abaixo:

 subtração, divisão, porcentagem, multiplicação, adição

*/

let N1 = parseInt(window.prompt("Insira 1º numero inteiro: "));
let N2 = parseInt(window.prompt("Insira 2º numeros interiro" ));

alert("Subtração: "+ (N1-N2));
alert("Divisão: "+ (N1/N2));
alert("Porcentagem: "+ (N1/N2*100)+"%");
alert("Multiplicação: "+ (N1*N2));
alert("Adição: " + (N1 + N2));

